function changeLanguage(lang) {
    if (!translations[lang]) return;
    document.documentElement.lang = lang;
    localStorage.setItem('preferredLanguage', lang);
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.setAttribute('placeholder', translations[lang][key]);
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Handle selects explicitly since options have values
    const selectEl = document.getElementById('inquiry');
    if (selectEl) {
        selectEl.options[0].text = translations[lang]['form_opt1'];
        selectEl.options[1].text = translations[lang]['form_opt2'];
        selectEl.options[2].text = translations[lang]['form_opt3'];
        selectEl.options[3].text = translations[lang]['form_opt4'];
    }
}
